const checkAllEmptySpaces = (str) => {
    for(character of str) {
        if(character !== '.') {
            return false;
        }
    }
    return true;
}

const getExpandables = (arr) => {
    var expandRows = [];

    for(var i = 0; i < arr.length; i++) {
        if(checkAllEmptySpaces(arr[i])) {
            expandRows.push(i)
        }
    }

    var expandCols = [];
    for(var i = 0; i <= arr.length - 1; i++) {
        var column = '';
        for(var j = 0; j <= arr[i].length - 1; j++) {
            column += arr[j][i];
        }
        if(checkAllEmptySpaces(column)) {
            expandCols.push(i)
        }
    }

    return {
        row: expandRows,
        column: expandCols
    };
}

const getIndexMap = (array) => {
    const indexMap = [];
    for(var row = 0; row < array.length; row++) {
        for(var col = 0; col < array[row].length; col++) {
            if(array[row].charAt(col) === '#') {
                indexMap.push([row, col])
            }
        }
    }

    return indexMap;
}

const getShortestPathLengths = (indexMap, row, column) => {
    var ans = 0;
    for(var i = 0; i < indexMap.length; i++) {
        for(var j = i+1; j < indexMap.length; j++) {
            ans += Math.abs(indexMap[i][0] - indexMap[j][0]) + Math.abs(indexMap[i][1] - indexMap[j][1])
            var minRow = Math.min(indexMap[i][0], indexMap[j][0])
            const maxRow = Math.max(indexMap[i][0], indexMap[j][0])
            var minCol = Math.min(indexMap[i][1], indexMap[j][1])
            const maxCol = Math.max(indexMap[i][1], indexMap[j][1])

            while(minRow !== maxRow) {
                if(row.includes(minRow)) {
                    ans += 999999;
                }
                minRow++;
            }
            while(minCol !== maxCol) {
                if(column.includes(minCol)) {
                    ans += 999999;
                }
                minCol++;
            }
        }
    }

    return ans;
}

