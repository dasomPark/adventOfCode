const findColMirror = (arr) => {
    const pattern = arr[0];
    const middleIndex = Math.floor((pattern.length - 2) / 2);
    const isEven = pattern.length % 2 === 0
    var width = 0;
    var ans = []

    for(let i = 0; i < pattern.length - 1; i++) {
        const newPattern = pattern.substring(i - width, i + width + 2) 
        if(newPattern === newPattern.split("").reverse().join("")) {
            var match = true;
            for(let j = 1; j < arr.length ; j++) {
                const newPatternInNextRow = arr[j].substring(i - width, i + width + 2);
                if(newPatternInNextRow !== newPatternInNextRow.split("").reverse().join("")) {
                    match = false;
                }
            }
            if(match) {
                ans.push(i+1)
            }
        }

        if(i === middleIndex) {
            if(isEven) {
                width--;
            }
        } else if ((!isEven && i === middleIndex + 1)) {
            width--;
        } else if(i < middleIndex) {
            width++;
        } else {
            width--;
        }
    }

    return ans;
}

