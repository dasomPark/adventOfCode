const simplifyInput = (inputData) => {
    const inputArray = inputData.split(`\n`);
    var newInput = [];
    inputArray.forEach((element, i) => {
        for(let j = 0; j < element.length; j++) {
            const character = element[j];
            if(!newInput[j]) {
                newInput[j] = character;
            } else {
                newInput[j] += character;
            }
            
        }
    })

    return newInput;
}

const rollNorth = (input) => {
    const ans = [];
    input.map(location => {
        var newLocation = '';
        var roundTemp = '';
        var cubeTemp = '';
        for(let i = 0; i < location.length; i++) {
            const character = location[i];
            if(character === 'O') {
                roundTemp += 'O'
            } else if(character === '.') {
                cubeTemp += '.'
            } else if(character === '#') {
                newLocation += roundTemp + cubeTemp + '#';
                roundTemp = '';
                cubeTemp = '';
            }
            
            if(i === location.length - 1) {
                newLocation += roundTemp + cubeTemp;
            }
        }
        ans.push(newLocation)
    })

    return ans;
}

const calculateTotalLoad = (input) => {
    var total = 0;
    input.map(location => {
        for(let i = 0; i < location.length; i++) {
            if(location[i] === 'O') {
                total += location.length - i;
            }
        }
    })

    return total;
}

